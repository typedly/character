/*
  Quotation marks and apostrophe
  ---
  You’ll find these punctuation marks hanging out at the top of a line of text.
*/

/**
 * @description You’ll find these punctuation marks hanging out at the top of a line of text.
 * @export
 */
export type QuotationMark =
  | '"' // Quotation - The most common use of quotation marks is to contain quotations.
  | '‘'  // Left single quotation mark
  | '’' // Right single quotation mark
  | '“'  // Left quotation mark
  | '”'  // Right quotation mark
  | "'" // Apostrophe - The apostrophe is most often used to form possessives and contractions.
;
